package com.i2iproject.builders.models;

public class ItemsData {
	private String name;
	private String value;
	
	public ItemsData(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}
	
}
